const mysql = require('mysql');

const UserSchema = new mysql.Schema({
    name: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    password: {
        type: String,
        required: true
    },
    date: {
        type: Date,
        default: Date.now
    }
});

const User = mysql.model('User', UserSchema);
module.exports = User;